<?php

namespace Acme\DemoTwoBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Transaccion
 */
class Transaccion
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var integer
     */
    private $tipoTransaccion;

    /**
     * @var \DateTime
     */
    private $fechaTransaccion;

    /**
     * @var string
     */
    private $encargado;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set tipoTransaccion
     *
     * @param integer $tipoTransaccion
     * @return Transaccion
     */
    public function setTipoTransaccion($tipoTransaccion)
    {
        $this->tipoTransaccion = $tipoTransaccion;

        return $this;
    }

    /**
     * Get tipoTransaccion
     *
     * @return integer 
     */
    public function getTipoTransaccion()
    {
        return $this->tipoTransaccion;
    }

    /**
     * Set fechaTransaccion
     *
     * @param \DateTime $fechaTransaccion
     * @return Transaccion
     */
    public function setFechaTransaccion($fechaTransaccion)
    {
        $this->fechaTransaccion = $fechaTransaccion;

        return $this;
    }

    /**
     * Get fechaTransaccion
     *
     * @return \DateTime 
     */
    public function getFechaTransaccion()
    {
        return $this->fechaTransaccion;
    }

    /**
     * Set encargado
     *
     * @param string $encargado
     * @return Transaccion
     */
    public function setEncargado($encargado)
    {
        $this->encargado = $encargado;

        return $this;
    }

    /**
     * Get encargado
     *
     * @return string 
     */
    public function getEncargado()
    {
        return $this->encargado;
    }
}
